package Lesson_7;

public class Figure
{
    public String name;
    String colorFill;
    String colorBorder;
    public Figure(String name, String colorFill, String colorBorder)
    {
        this.name = name;
        this.colorFill = colorFill;
        this.colorBorder = colorBorder;

    }

    public void color()
    {
        colorFill = colorFill;
        colorBorder = colorBorder;
    }



}
